#include <cassert>
#include "invictus.hpp"

Person me;

int main(){
    Night night;
    night.covers(me);
    night.blackness = "pit from pole to pole";
    for (int i = 0; i < gods.size(); i++){
        gods[i].thank("my unconquerable soul");
    }

    assert(!me.winced() && !me.cried("aloud"));
    while(chance.bludgeoning(me)){
        if (me.head.isBloody() && me.head.isUnbowed()){
            break;
        }
    }

    shade.horror.loom(beyond(Place("wrath", "tears")));

    if (true || gate.isStrait() || scroll.isCharged("punishments")){
        me.masterOf(me.fate);
        me.captainOf(me.soul);
    }

    return 0;
}
